<p>Since 2005 we have designed, built and tested custom capacitor banks for a variety of applications in pulsed power and plasma physics.  We have built pulse forming networks, RLC circuits, and designed Marx banks, and Blumlein banks for high power applications for customers at National Laboratories, universities and also in the private sector. We can provide a full design for your new application, or reverse engineer for an old system that may have failed.  We can provide full a full controls system, entailing interlocks also.  We have two main fabrication spaces for pulsed power, including electronics workshop and HV testing areas.</p>

